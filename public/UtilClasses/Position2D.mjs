export default class Position2D {
    constructor(xPos, yPos) {
        this.xPos = xPos;
        this.yPos = yPos;
    }

    getXPos() {
        return this.xPos;
    }

    getYPos() {
        return this.yPos;
    }

    setXPos(xPos) {
        this.xPos = xPos;
    }

    setYPos(yPos) {
        this.yPos = yPos;
    }
}