export default class Colour {
    constructor(r, g, b) {
        this.r = r;
        this.g = g;
        this.b = b;
    }

    getR() {
        return this.r;
    }

    getG() {
        return this.g;
    }

    getB() {
        return this.b;
    }

    setR(r) {
        this.r = r;
    }

    setG(g) {
        this.g = g;
    }

    setB(b) {
        this.b = b;
    }
}